type TranslationKey = keyof typeof translations.zh

interface LanguageContextType {
  language: Language
  setLanguage: (lang: Language) => void
  t: (key: TranslationKey, params?: Record<string, string | number>) => string
}

const LanguageContext = createContext<LanguageContextType | null>(null)

export function useLanguage(): LanguageContextType {
  const context = useContext(LanguageContext)
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider')
  }
  return context
}

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguageState] = useState<Language>('zh')

  useEffect(() => {
    const loadLanguage = async (): Promise<void> => {
      const savedLang = await window.api.getSettings('language')
      if (savedLang === 'en' || savedLang === 'zh') {
        setLanguageState(savedLang)
      }
    }
    loadLanguage()
  }, [])

  const setLanguage = useCallback(async (lang: Language) => {
    setLanguageState(lang)
    await window.api.saveSettings('language', lang)
  }, [])

  const t = useCallback((key: TranslationKey, params?: Record<string, string | number>): string => {
    let text: string = translations[language][key] || translations.zh[key] || key
    if (params) {
      for (const [paramKey, paramValue] of Object.entries(params)) {
        text = text.replace(`{${paramKey}}`, String(paramValue))
      }
    }
    return text
  }, [language])

  const value = React.useMemo(() => ({ language, setLanguage, t }), [language, setLanguage, t])

  return React.createElement(LanguageContext.Provider, { value }, children)
}
